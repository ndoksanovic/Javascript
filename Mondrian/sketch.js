function setup()
{
	//create a large square canvas
	createCanvas(800, 800);
}

function draw()
{

	//set the fill colour to red
	fill(250, 0, 0);
	

	//set a thick stroke weight for the black lines
	strokeWeight(12);

	//draw the red rectangle 
	rect(100, 50, 500, 500);
	fill(0,0,300);
    rect(100, 425, 100, 125);
    fill(250,700,350);
    rect(100, 50, 100, 200);
    rect(200, 425, 350, 125);
    rect(550, 425, 50, 60);
    rect(100, 225, 100, 200);
    fill(250, 635, 120);
    rect(550, 480, 50, 65);
    

}